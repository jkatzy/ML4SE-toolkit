#!/usr/bin/env python3
"""Build adversarial comment-testing prompt packets for supported languages."""

from __future__ import annotations

import argparse
import json
import math
from dataclasses import dataclass
from pathlib import Path

from build_comment_language_fixtures import FIXTURE_DIR, language_fixture_filename

from ml4setk import get_supported_comment_languages
from ml4setk.Parsing.Comments import get_comment_syntax

ROOT = Path(__file__).resolve().parents[1]
TESTING_DIR = ROOT / "docs" / "comment_testing"
PROMPT_DIR = TESTING_DIR / "prompt_packets"
THREAD_DIR = TESTING_DIR / "threads"
ASSIGNMENTS_PATH = TESTING_DIR / "agent_chunk_assignments.json"
DEFAULT_CHUNK_COUNT = 8


@dataclass(frozen=True)
class LanguageContext:
    """One supported language with the registry family that implements it."""

    language: str
    family_name: str
    fixture_path: str
    notes: str


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Build adversarial comment-testing prompt packets."
    )
    parser.add_argument(
        "--chunk-count",
        type=int,
        default=DEFAULT_CHUNK_COUNT,
        help="Number of language chunks to generate.",
    )
    return parser.parse_args()


def build_language_contexts() -> list[LanguageContext]:
    contexts = []
    for language in get_supported_comment_languages():
        syntax = get_comment_syntax(language)
        contexts.append(
            LanguageContext(
                language=language,
                family_name=syntax.family_name,
                fixture_path=(FIXTURE_DIR / language_fixture_filename(language)).as_posix(),
                notes=syntax.notes,
            )
        )
    return contexts


def chunk_contexts(
    contexts: list[LanguageContext], chunk_count: int
) -> list[tuple[str, list[LanguageContext]]]:
    if chunk_count < 1:
        raise ValueError("chunk_count must be at least 1")
    if not contexts:
        return []

    chunk_size = math.ceil(len(contexts) / chunk_count)
    chunks = []
    for index, start in enumerate(range(0, len(contexts), chunk_size)):
        chunks.append((f"chunk_{index}", contexts[start : start + chunk_size]))
    return chunks


def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def maybe_write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not path.exists():
        path.write_text(content, encoding="utf-8")


def render_language_scope(contexts: list[LanguageContext]) -> str:
    lines = ["| Language | Family | Fixture | Notes |", "| --- | --- | --- | --- |"]
    for context in contexts:
        notes = context.notes.replace("|", "\\|") if context.notes else ""
        if not notes:
            notes = "-"
        lines.append(
            f"| `{context.language}` | `{context.family_name}` | "
            f"`{context.fixture_path}` | {notes} |"
        )
    return "\n".join(lines)


