def render_findings_skeleton(chunk_name: str, contexts: list[LanguageContext]) -> str:
    lines = [
        f"# Adversarial Findings: {chunk_name}",
        "",
        "Generated by `scripts/build_comment_test_packets.py`. This file is",
        "breaker-owned.",
        "",
        "## Scope",
        "",
        f"- Language count: `{len(contexts)}`",
        "- Status: `pending` until findings are recorded",
        "",
        "## Languages",
        "",
    ]
    for context in contexts:
        lines.extend(
            [
                f"### {context.language}",
                "",
                f"- Family: `{context.family_name}`",
                f"- Fixture file: `{context.fixture_path}`",
                "- Status: pending",
                "- Findings:",
                "  - Case: TODO",
                "  - Fixture edit: TODO",
                "  - Fixture parse check: TODO",
                "  - Extraction entry point: TODO",
                "  - Sanitizer entry point: TODO | n/a",
                "  - Input: TODO",
                "  - Raw extracted match: TODO",
                "  - Actual behavior: TODO",
                "  - Sanitized output: TODO | n/a",
                "  - Expected behavior: TODO",
                "  - Expected sanitized output: TODO | n/a",
                "  - Removal contract: TODO",
                "  - Preservation contract: TODO",
                "  - Paired preservation testcase: TODO",
                "  - Why it matters: TODO",
                "",
            ]
        )
    return "\n".join(lines)


